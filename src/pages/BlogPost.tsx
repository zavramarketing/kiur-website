export default function BlogPost() {
  const { slug } = useParams<{ slug: string }>();
  const post = slug ? posts[slug] : null;

  if (!post) {
    return (
      <div className="bg-background min-h-[calc(100vh-4rem)] flex items-center justify-center">
        <p className="text-primary/40 text-lg font-medium">Статья не найдена</p>
      </div>
    );
  }

  return (
    <div className="bg-background min-h-[calc(100vh-4rem)] py-10 md:py-16">
      <div className="max-w-2xl mx-auto px-4 sm:px-6 lg:px-8">
        <Link
          to="/blog"
          className="inline-flex items-center gap-2 text-primary/50 hover:text-primary text-sm mb-8 transition-colors"
        >
          <ArrowLeft className="w-4 h-4" />
          Все статьи
        </Link>

        <div className="flex items-center gap-1.5 text-primary/40 text-sm mb-4">
          <CalendarDays className="w-4 h-4" />
          <span>{post.date}</span>
        </div>

        <h1 className="font-heading text-2xl md:text-4xl font-bold text-primary mb-10 leading-snug">
          {post.title}
        </h1>

        <div className="space-y-5 text-primary/80 leading-relaxed">
          {post.content.map((block, i) => {
            if (block.type === 'h2') {
              return (
                <h2 key={i} className="font-heading text-lg md:text-xl font-bold text-primary mt-8 mb-2">
                  {block.text}
                </h2>
              );
            }
            if (block.type === 'ul') {
              return (
                <ul key={i} className="space-y-2 pl-1">
                  {block.items?.map((item, j) => (
                    <li key={j} className="flex items-start gap-2">
                      <span className="mt-1.5 w-1.5 h-1.5 rounded-full bg-primary/40 shrink-0" />
                      <span>{item}</span>
                    </li>
                  ))}
                </ul>
              );
            }
            return <p key={i}>{block.text}</p>;
          })}
        </div>

        <div className="mt-14 pt-8 border-t border-primary/10 text-center">
          <p className="text-primary/60 mb-4">Хотите увидеть это своими глазами?</p>
          <Link
            to="/tours"
            className="inline-block bg-primary text-background px-7 py-3 rounded-full font-medium hover:bg-primary/90 transition-colors"
          >
            Смотреть туры
          </Link>
        </div>
      </div>
    </div>
  );
}
